char	*ft_strchr(const char *s, int c)
{
	int	i;
	
	i = 0;
	while (s[i])
	{
		if (c == s[i])
			return ((char *)&s[i]);
		if (c == 0 && s[i + 1] == 0)
			return ((char *)&s[i + 1]);
		i++;
	}
	if (c == 0)
		return ((char *)s);
	return (0);
}
