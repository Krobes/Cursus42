#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*s;

	s = (char *)b;
	i = 0;
	while (len > 0)
	{
		s[i] = c;
		len--;
		i++;
	}
	return (s);
}
